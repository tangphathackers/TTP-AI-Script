set_adaptive_cpu_properties() {
while true; do
# Using load average
load_avg=$(cat /proc/loadavg | cut -d ' ' -f 1)
# Using uptime
uptime_load=$(uptime | awk -F'average:' '{ print $2 }' | awk -F, '{ print $2 }' | cut -d ' ' -f 2)
# Using top command
top_load=$(top -bn 1 | grep "Cpu(s)" | awk '{print $2 + $4}')
# Calculate the average load from the three methods
avg_load=$((($load_avg + $uptime_load + $top_load) / 3))
# Custom logic for setting CPU frequencies based on average load
if (( $avg_load < 1 )); then
cpu_freqs="0.3,0.4,0.5,0.6,0.7,0.8,0.9,1.0"
elif (( $avg_load < 2 )); then
